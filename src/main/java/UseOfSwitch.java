public class UseOfSwitch {
	public static void main(String args[]){
		//int x=1;
		String x="man";
		switch(x)
		{
		case "man":
		System.out.println("x is less than 10");
		break;
		case "animal":
		System.out.println("x is greater than 10");
		break;
		default:
			System.out.println("default");
		
		}
		
	}

}
